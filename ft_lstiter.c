#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (!lst || !f)
		return ;
	while (lst)
	{
		f(lst->content);
		lst = lst->next;
	}
}
//itera la lista y aplica f a cada nodo
//mientras haya nodos, aplico f al nodo actual lst->content
//y luego actualizo lst al siguiente, como si fuera lst++